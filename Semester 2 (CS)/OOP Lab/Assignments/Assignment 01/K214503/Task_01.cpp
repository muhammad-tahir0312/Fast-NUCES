#include<iostream>
using namespace std;
int main()
{
	
	int a;
	float b;
	char c;
	bool d;
		
	cout << "Enter INT Value: ";
	cin >> a; cout << endl;
	cout << "Enter FLOAT Value: ";
	cin >> b; cout << endl;	
	cout << "Enter CHAR Value: ";	
	cin >> c; cout << endl;
	cout << "Enter BOOL Value: ";
	cin >> d; cout << endl;
	
	cout << "INT Value: " << a << endl;
	cout << "FLOAT Value: " << b << endl;	
	cout << "CHAR Value: " << c << endl;	
	cout << "BOOL Value: " << d << endl;	
	
	return 0;
}

/* Primitive datatypes are int, char, float,
   void etc. While derived datatypes are 
   composed of primitive datatypes For example; 
   arrays, structures, pointers etc.
*/
